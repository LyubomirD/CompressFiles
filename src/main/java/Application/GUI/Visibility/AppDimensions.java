package Application.GUI.Visibility;

public class AppDimensions {
    private final int width;
    private final int height;

    public AppDimensions(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }
    public int getHeight() {
        return height;
    }

}
